                                                                /* ============= GLOBAL ELEMENT REFERENCES ============ */

let userScore = parseInt(localStorage.getItem("userScore"));
let userInitials = document.getElementById("userInitials");
let submitButton = document.getElementById("initialSubmitBtn");
let finalScoreNotification = document.getElementById("finalScoreNotice");


                                                                /* ==================== FUNCTIONS ===================== */

                                                                /* Show user final score                                */
finalScoreNotification.innerHTML = "Your final score is " + userScore;


/**
 * Event listener for submit button. Submit button will
 * clear the temporary user score saved throughout the
 * quiz, save that score to a key value of the user's
 * initials, and load the "High Scores" page.
 */
submitButton.addEventListener("click", function (event) {
    let element = event.target;

    event.preventDefault();                                     /* Prevent the submit button from refereshing page      */

    if (element.matches("button"))                              /* Ensure event was generated by Submit button          */
    {
        localStorage.setItem(userInitials.value, userScore);    /* Save score under user initials in local storage      */
        localStorage.removeItem("userScore");                   /* Remove previously used user score from quiz page     */
        window.location = "./high-scores.html";                 /* Load High SCores page after submitting initials      */
    }
});
